/*
假設你下載了一個新的角色 Sprite：

1. 在瀏覽器中開啟圖表圖片
2. 右鍵 → 檢查元素 (Inspect)
3. 在開發者工具中找到 <img> 標籤
4. 查看顯示的自然寬度和高度
   
   例如：
   <img src="ryu.png" width="1440" height="128">
   
   這表示：
   - 總寬度：1440 像素
   - 高度：128 像素

5. 數一下有多少幀動作（連續的角色姿態）
   假設有 9 幀

6. 在 sketch.js 的配置中設置：
   const CHARACTER1_CONFIG = {
     stop: { width: 1440, height: 128, frames: 9 },
     ...
   }
*/

